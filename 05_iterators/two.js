// while loop

let index=0;
while(index<=10){
    console.log(`index is ${index}`);
    index=index+1
}

// while loop over an array

myarr=["flash","batman","superman"]
let i=0;
while(i<myarr.length){
    console.log(myarr[i]);
    i=i+1
}

// do while loop

let int=0
do {
    console.log("hi");
    int++
} while (int<10);