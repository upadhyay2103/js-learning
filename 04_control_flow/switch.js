// above is the basic structure of a switch statement

const key=3
switch (key) {
    case 1:
        console.log("January");
        break;
    case 2:
        console.log("Feb");
        break;
    case 3:
        console.log("March");
        break;
    case 4:
        console.log("April");
        break;

    default:
        break;
}

// keep in mind that this key need not to be number always it can be string, char etc as well

